    private void jButtonAgregarCursoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonAgregarCursoActionPerformed

        if (!validar()) {
            return;
        }
        
        Curso curso=new Curso(
                jTextFieldTitulo.getText(),
                jTextFieldProfesor.getText(),
                jComboBoxDia.getItemAt(jComboBoxDia.getSelectedIndex()),//Nos devuelve un objeto del tipo dia segun el indice seleccionado
                jComboBoxTurno.getItemAt(jComboBoxTurno.getSelectedIndex())
        );
        //Guardamos
        cursoRepository.save(curso);
        //Lo mostramos en el input de la gui
        jLabelOutput.setText("Se dio de alta el curso con el id:"+curso.getId());
        //Limpiamos la salida de textField
        limpiar();   
        CargarElementosPrivados();
    }
    
    private void limpiar(){
        jTextFieldTitulo.setText("");
         jTextFieldTitulo.requestFocus();
        jTextFieldProfesor.setText("");
        jComboBoxDia.setSelectedIndex(0);
        jComboBoxTurno.setSelectedIndex(0);
        
        
    }
    private boolean validar() {
        
        if (!new Validator(jTextFieldTitulo).length(3, 10)) {
            return false;
        }
        if (!new Validator(jTextFieldProfesor).length(3, 10)) {
            return false;
        }
        return true;
    
    }//GEN-LAST:event_jButtonAgregarCursoActionPerformed

