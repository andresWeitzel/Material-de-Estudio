    private void CargarElementosPrivados() {
        //Cargamos los elementos de los comboboxs
        //Cargar dia
        jComboBoxDia.removeAllItems();
        for (Dia d : Dia.values()) {
            jComboBoxDia.addItem(d);
        }

        //cargar Turno
        jComboBoxTurno.removeAllItems();
        for (Turno t : Turno.values()) {
            jComboBoxTurno.addItem(t);
        }
        //Cargar tabla cursos
        new Table().cargar(jTableCursos, cursoRepository.getAll());
    }

