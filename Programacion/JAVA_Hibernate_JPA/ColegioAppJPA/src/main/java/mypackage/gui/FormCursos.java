    private void jButtonElimiinarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonElimiinarActionPerformed
        int row=jTableCursos.getSelectedRow();
        if(row == -1){
            return;
        }
        //Se pide la fila con el id que queremos eliminar y con el cero se elimina
        int id=(int)jTableCursos.getValueAt(row,0);
        //System.out.println("Id eliminado:"+id);
        if(JOptionPane.showConfirmDialog(this, "Se borrara el curso con el id "+id+"!!")!=0){
            return;
        }
        Curso objetoCurso=cursoRepository.getById(id);
        //Pedimos una lista de alumnos 
        if(alumnoRepository.getByCurso(objetoCurso).size()!=0){
            JOptionPane.showMessageDialog(this,"El curso no se puede eliminar porque contiene alumnos!","Error",JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        
        //Borramos de la lista el elemento con el id pasadoe n el buscador
        cursoRepository.remove(objetoCurso);
        //Cargamos los elementos nuevamente
          CargarElementosPrivados();
        
    }//GEN-LAST:event_jButtonElimiinarActionPerformed

    private void jButtonMostrarAlumnosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonMostrarAlumnosActionPerformed
        int row=jTableCursos.getSelectedRow();
        if(row == -1){
            return;
        }
        //Se pide la fila con el id que queremos eliminar y con el cero se elimina
        int id=(int)jTableCursos.getValueAt(row,0);
        
        Curso objetoCurso=cursoRepository.getById(id);
        
        //Listo los alumnos
        ListarAlumnos listarAlumnos=new ListarAlumnos(objetoCurso);
        //Obtengo el desktop padre
        this.getParent().add(listarAlumnos);
        //Lo hacemos visible
        listarAlumnos.setVisible(true);
    }//GEN-LAST:event_jButtonMostrarAlumnosActionPerformed



